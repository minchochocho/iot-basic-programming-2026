// 구구단
#include<stdio.h>

int main() {
	int i, j;

	for (int i = 2; i < 10; i++)
	{
		printf("%d단\n", i);
		printf("--------------------------------------------------------------------------------------------------------------------------------------\n");
		for (j = 1; j < 10; j++)
		{
			printf("%d x %d = %2d\t", i, j, i * j);
		}
		puts("");
		puts("");
	}


	return 0;
}